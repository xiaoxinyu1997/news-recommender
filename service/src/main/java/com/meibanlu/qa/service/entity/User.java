package com.meibanlu.qa.service.entity;

public class User {
    private int userid;
    private String phonenumber;
    private String nickname;
    private String age;
    private String sex;
    private String city;
    private String registerTime;
    private String newspreferences;
    private String musicpreferences;
    private String password;
    private String avatar;

    public int getUserid() {
        return userid;
    }

    public void setUserid(int userid) {
        this.userid = userid;
    }

    public String getPhonenumber() {
        return phonenumber;
    }

    public void setPhonenumber(String phonenumber) {
        this.phonenumber = phonenumber;
    }

    public String getNickname() {
        return nickname;
    }

    public void setNickname(String nickname) {
        this.nickname = nickname;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getRegisterTime() {
        return registerTime;
    }

    public void setRegisterTime(String registerTime) {
        this.registerTime = registerTime;
    }

    public String getNewspreferences() {
        return newspreferences;
    }

    public void setNewspreferences(String newspreferences) {
        this.newspreferences = newspreferences;
    }

    public String getMusicpreferences() {
        return musicpreferences;
    }

    public void setMusicpreferences(String musicpreferences) {
        this.musicpreferences = musicpreferences;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }
}
